/* event for done interrupt */
EventGroupHandle_t i2s_sample_event;

/* Termination Callback */
void i2s_term_handler(void)
{
    BaseType_t xHigherPriorityTaskWoken;
    xHigherPriorityTaskWoken = pdFALSE;
    xEventGroupSetBitsFromISR(i2s_sample_event, (0x1), &xHigherPriorityTaskWoken);
}

void i2s_sample(void *param)
{
    DA16X_UNUSED_ARG(param);

    /* DO SOMETHING */
    HANDLE gi2shandle = NULL;
    I2S_HANDLER_TYPE *i2s;
    unsigned int mode, data;
    unsigned int callback_ioctl_data[3];

#ifdef	I2S_SAMPLE_SET_MODE_RX
    unsigned int rd_len;
    UINT8	rx_buf[2][768] = {0,};
#endif

#ifdef	I2S_SAMPLE_SET_MODE_RX
    mode = I2S_MODE_RX;
#else
    mode = I2S_MODE_TX;
#endif

    PRINTF("I2S_SAMPLE\n");

    DA16X_CLOCK_SCGATE->Off_DAPB_I2S = 0;
    DA16X_CLOCK_SCGATE->Off_DAPB_APBS = 0;

    gi2shandle = DRV_I2S_CREATE(I2S_0);
    i2s = (I2S_HANDLER_TYPE *) gi2shandle;

    if (!gi2shandle) {
        vTaskDelete(NULL);
        return;
    }

    /* Set I2S Output Mode */
    if (DRV_I2S_INIT(gi2shandle, mode) == FALSE) {
        vTaskDelete(NULL);
        return;
    }

    // GPIO[3] - I2S_LRCK,  GPIO[2] - I2S_SDO
    _da16x_io_pinmux(PIN_BMUX, BMUX_I2S);
    // GPIO[1] - I2S_MCLK,  GPIO[0] - I2S_BCLK
    _da16x_io_pinmux(PIN_AMUX, AMUX_I2S);

    DRV_I2S_SET_CLOCK(gi2shandle, I2S_CLK_SOURCE_INTERNAL, 0);

    data = TRUE;
    DRV_I2S_IOCTL(i2s, I2S_SET_STEREO, &data); /* Set Stereo Output Mode */
#ifdef	I2S_SAMPLE_SET_MODE_RX
    data = I2S_RESOLUTION_RX_16B;
#else
    data = I2S_RESOLUTION_TX_16B;
#endif

    DRV_I2S_IOCTL(i2s, I2S_SET_PCM_RESOLUTION, &data); /* Set 16bit resolution Mode */

    callback_ioctl_data[0] = I2S_CB_TERM;
    callback_ioctl_data[1] = (unsigned int)i2s_term_handler;
    callback_ioctl_data[2] = 0;

    /* Register Termination Callback */
    DRV_I2S_IOCTL(i2s, I2S_SET_HISR_CALLACK, (void *)callback_ioctl_data);

    /* Create Event */
    i2s_sample_event = xEventGroupCreate();

    /* 2 Times I2S Transaction */
    for (int i = 0; i < 2; i++) {
#ifdef	I2S_SAMPLE_SET_MODE_RX
        rd_len = DRV_I2S_READ(i2s, (unsigned int *)rx_buf[i], 768, 0);
#else
        DRV_I2S_WRITE(i2s, (unsigned int *) sinewave_pattern, 768, 0);
#endif
        xEventGroupWaitBits(i2s_sample_event, 0x1, pdTRUE, pdFALSE, 20);
    }

#ifdef	I2S_SAMPLE_SET_MODE_RX
    PRINTF("read len = %d\r\n", rd_len);
    for (int i = 0; i < 768 * 2; i++) {
        if (!(i % 8)) {
            PRINTF("\r\n");
        }
        PRINTF("0x%02x ", rx_buf[i / 768][i % 768]);
    }
    PRINTF("\r\n");
#endif

    vTaskDelete(NULL);

    return ;
}
