UINT factory_reset_default(int reboot_flag)
{
    PRINTF("\n\33[31mFactory Reset...\n");

#if defined( __SUPPORT_FACTORY_RST_APMODE__ )
  #if defined( __SUPPORT_WIFI_PROVISIONING__ )	// For Mobile-App
    /* 1: AP_SECURITY_MODE  1: remove NVRAM for APP_Provision */
    app_reboot_ap_mode(SYSMODE_AP_ONLY, 1, 1);
  #else
    factory_reset_ap_mode();
  #endif  // __SUPPORT_WIFI_PROVISIONING__
#elif defined( __SUPPORT_FACTORY_RST_STAMODE__ )
    factory_reset_sta_mode();
#elif defined( __SUPPORT_FACTORY_RST_CONCURR_MODE__ )
    factory_reset_concurrent_mode();
#else
    factory_reset_user_define();
#endif /* __SUPPORT_FACTORY_RST_APMODE__*/

    if (reboot_flag == pdTRUE) {
        PRINTF("\nRebooting....");
    }

    PRINTF("\33[0m\n\n");

    vTaskDelay(10);

    if (reboot_flag == pdTRUE) {
#if defined ( __BLE_COMBO_REF__ )
        combo_ble_sw_reset();
#endif    // __BLE_COMBO_REF__
        reboot_func(SYS_REBOOT_POR);

        /* Wait for system-reboot */
        while (1) {
            vTaskDelay(10);
        }
    }

    return ERR_OK;
}


