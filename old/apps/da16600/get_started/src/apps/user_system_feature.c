/*
 * Information for Console Version display
 */
void version_display(int argc, char *argv[])
{
    UINT32  sysclock;
    UCHAR   rtos_img_ver_str[DA16X_IH_NMLEN+1] = { 0, };
    UINT    rtos_img_ver_str_len = 0;
    int     boot_index = 0;
#if defined (__SUPPORT_OTA__)
    FW_versionInfo_t fw_ver;
#endif /* __SUPPORT_OTA__ */

    DA16X_UNUSED_ARG(argc);
    DA16X_UNUSED_ARG(argv);

    _sys_clock_read(&sysclock, sizeof(UINT32));
    sysclock = sysclock / MHz;

#if defined __CPU_CLOCK_FIXED__ // CPU Clock 120MHz
    if (sysclock != FIXED_CLOCK) {
        do_write_clock_change(FIXED_CLOCK * MHz, pdTRUE);
        vTaskDelay(10);

        reboot_func(SYS_REBOOT_POR);

        /* Wait for system-reboot */
        while (1) {
            vTaskDelay(10);
        }
    }
#endif // __CPU_CLOCK_FIXED__

    rtos_img_ver_str_len = get_firmware_version(2, rtos_img_ver_str);    // RTOS
    boot_index = get_cur_boot_index();

    PRINTF("\n\n");
    PRINTF("\t******************************************************\n");
    PRINTF("\t*             %s SDK Information\n", CHIPSET_NAME);
    PRINTF("\t* --------------------------------------------------- \n");
    PRINTF("\t*\n");
    PRINTF("\t* - CPU Type        : Cortex-M4 (%dMHz)\n", sysclock);
    PRINTF("\t* - OS Type         : FreeRTOS %d.%d.%d\n", FREERTOS_MAJOR_VERSION, FREERTOS_MINOR_VERSION, FREERTOS_SUB_VERSION);
    PRINTF("\t* - Serial Flash    : 4 MB\n");
    PRINTF("\t* - SDK Version     : V%d.%d.%d.%d %s\n", SDK_MAJOR, SDK_MINOR, SDK_REVISION, SDK_ENG_VER, SDK_NAME);
    if (rtos_img_ver_str_len > 0) {
        PRINTF("\t* - F/W Version     : %s\n", rtos_img_ver_str);
#if defined (__SUPPORT_OTA__)
        if (ota_update_parse_version_string(rtos_img_ver_str, &fw_ver) != OTA_SUCCESS) {
            PRINTF("\t*\n");
            PRINTF("\t*       !! F/W Version format is incorrect !!\n");
            PRINTF("\t*       Type        : %s\n", fw_ver.update_type);
            PRINTF("\t*       Vendor      : %s\n", fw_ver.vendor);
            PRINTF("\t*       Major       : %s\n", fw_ver.major);
            PRINTF("\t*       Minor       : %s\n", fw_ver.minor);
            PRINTF("\t*       Customer    : %s\n", fw_ver.customer);
            PRINTF("\t*\n");
        }
#endif /* __SUPPORT_OTA__ */
    } else {
        PRINTF("\t* - F/W Version     : Unknown - Fatal error !\n");
    }

    PRINTF("\t* - F/W Build Time  : %s %s\n", __DATE__ , __TIME__ );
    PRINTF("\t* - Boot Index      : %d\n", boot_index);
    PRINTF("\t*\n");
    PRINTF("\t******************************************************\n");
    PRINTF("\n");
}

